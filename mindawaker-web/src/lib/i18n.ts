export function translate(locale: Locale, key: TranslationKey, vars?: Record<string, string | number>) {
  const template = messages[locale][key] ?? messages.zh[key];
  if (!vars) return template;
  return Object.entries(vars).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

export function getLocaleDateTime(locale: Locale, value: number, options?: Intl.DateTimeFormatOptions) {
  return new Date(value * 1000).toLocaleString(htmlLangMap[locale], options);
}

export function getLocaleTime(locale: Locale, value: number, options?: Intl.DateTimeFormatOptions) {
  return new Date(value * 1000).toLocaleTimeString(htmlLangMap[locale], options);
}

export function getStatusLabel(locale: Locale, status?: string) {
  switch (status) {
    case 'completed':
      return translate(locale, 'completed');
    case 'error':
      return translate(locale, 'failed');
    case 'in_text_progress':
      return translate(locale, 'textStage');
    case 'in_audio_progress':
      return translate(locale, 'audioStage');
    case 'in_image_progress':
      return translate(locale, 'imageStage');
    case 'in_video_progress':
      return translate(locale, 'videoStage');
    case 'in_video_rebuild':
      return translate(locale, 'videoRebuildStage');
    case 'in_video_concat':
      return translate(locale, 'videoConcatStage');
    case 'running':
      return translate(locale, 'generating');
    default:
      return status || translate(locale, 'draft');
  }
}

